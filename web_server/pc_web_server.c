int send_str (int fd, char *msg)
{
	unsigned int numbytes, sent;   /* Number of byte send   */
    unsigned int total;

    total = strlen (msg);
    sent = 0;
    do 
    {
        /* Send file to host */
        if ((numbytes = send (fd, msg + sent, total - sent, 0)) < 0)
        { 
            printf("send() error\n");
            return -1; 
        }    
        else if (numbytes == 0)
        {   
            break;
        }
                
        sent += numbytes;
    
    } while (sent < total);
    
    return 0; /* successful */
}

